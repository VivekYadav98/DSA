class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        Node* curr1 = head1,*curr2=head2;
        unordered_map<int,int>m;
        while(curr2 != NULL)
        {
            m[curr2->data]++;
            curr2 = curr2->next;
        }
        
        Node* head = new Node(-1);
        Node* tail = head;
        while(curr1 != NULL)
        {
            if(m.find(curr1->data) != m.end())
            {
                tail->next = new Node(curr1->data);
                tail = tail->next;
            }
            curr1 = curr1->next;
        }
        
        return head->next;
    }
};

